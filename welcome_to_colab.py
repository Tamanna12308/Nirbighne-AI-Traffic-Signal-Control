import random
import shutil
from pathlib import Path

BASE = Path("/content/drive/MyDrive/New_folder/final_11class_combined")
SPLIT = Path("/content/final_11class_split")

shutil.rmtree(SPLIT, ignore_errors=True)

(SPLIT / "train/images").mkdir(parents=True, exist_ok=True)
(SPLIT / "train/labels").mkdir(parents=True, exist_ok=True)
(SPLIT / "valid/images").mkdir(parents=True, exist_ok=True)
(SPLIT / "valid/labels").mkdir(parents=True, exist_ok=True)
(SPLIT / "test/images").mkdir(parents=True, exist_ok=True)
(SPLIT / "test/labels").mkdir(parents=True, exist_ok=True)

images = [p for p in (BASE / "images").iterdir() if p.suffix.lower() in [".jpg", ".jpeg", ".png"]]

random.seed(42)
random.shuffle(images)

n = len(images)
train_end = int(0.8 * n)
valid_end = int(0.9 * n)

train_imgs = images[:train_end]
valid_imgs = images[train_end:valid_end]
test_imgs = images[valid_end:]

def copy_pair(img_list, img_dest, lbl_dest):
    for img in img_list:
        lbl = BASE / "labels" / f"{img.stem}.txt"
        if lbl.exists():
            shutil.copy2(img, img_dest / img.name)
            shutil.copy2(lbl, lbl_dest / lbl.name)

copy_pair(train_imgs, SPLIT / "train/images", SPLIT / "train/labels")
copy_pair(valid_imgs, SPLIT / "valid/images", SPLIT / "valid/labels")
copy_pair(test_imgs, SPLIT / "test/images", SPLIT / "test/labels")

print("Train:", len(list((SPLIT / 'train/images').glob('*'))))
print("Valid:", len(list((SPLIT / 'valid/images').glob('*'))))
print("Test:", len(list((SPLIT / 'test/images').glob('*'))))

