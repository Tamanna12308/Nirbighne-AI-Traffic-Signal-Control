import shutil
from pathlib import Path

BADODD = Path("/content/badodd")
OUT = Path("/content/badodd_clean_11")

shutil.rmtree(OUT, ignore_errors=True)
(OUT / "images").mkdir(parents=True, exist_ok=True)
(OUT / "labels").mkdir(parents=True, exist_ok=True)

image_exts = [".jpg", ".jpeg", ".png"]

badodd_to_11 = {
    0: 1,   # Auto_ricksaw
    1: 2,   # Bike
    2: 3,   # Bus
    6: 2,   # Bike
    11: 9   # Truck
}

images = [p for p in BADODD.rglob("*") if p.is_file() and p.suffix.lower() in image_exts]
labels = {p.stem: p for p in BADODD.rglob("*.txt")}

saved = 0

for img in images:
    lbl = labels.get(img.stem)
    if not lbl:
        continue

    new_lines = []

    with open(lbl, "r", encoding="utf-8", errors="ignore") as f:
        for line in f:
            parts = line.strip().split()
            if len(parts) == 5:
                cls = int(float(parts[0]))
                if cls in badodd_to_11:
                    parts[0] = str(badodd_to_11[cls])
                    new_lines.append(" ".join(parts))

    if new_lines:
        new_name = f"bad_{saved:06d}"
        shutil.copy2(img, OUT / "images" / f"{new_name}{img.suffix.lower()}")
        with open(OUT / "labels" / f"{new_name}.txt", "w") as f:
            f.write("\n".join(new_lines) + "\n")
        saved += 1

        if saved % 500 == 0:
            print("Processed:", saved)

print("FINAL saved:", saved)

